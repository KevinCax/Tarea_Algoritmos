
package pooalgortimos;


public class Especialidades extends Persona {
    
    private String bailar;
    private String cantar;

    /**
     * @return the bailar
     */
    public String getBailar() {
        return bailar;
    }

    /**
     * @param bailar the bailar to set
     */
    public void setBailar(String bailar) {
        this.bailar = bailar;
    }

    /**
     * @return the cantar
     */
    public String getCantar() {
        return cantar;
    }

    /**
     * @param cantar the cantar to set
     */
    public void setCantar(String cantar) {
        this.cantar = cantar;
    }
    
}
